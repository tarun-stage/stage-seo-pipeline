#!/usr/bin/env node
/**
 * update-webapp-seo.mjs
 *
 * Reads trending keyword data from gsc_keywords.db and applies keyword changes
 * directly to stage-webapp:
 *
 *  1. Writes src/config/seo-keywords.ts  — exported trending keywords per dialect
 *  2. Patches src/app/[lang]/[dialect]/page.tsx — adds `keywords` to generateMetadata
 *
 * Usage:
 *   node scripts/update-webapp-seo.mjs \
 *     --db gsc_keywords.db \
 *     --webapp ../../stage-webapp \
 *     --limit 10
 */

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { resolve, join } from 'node:path';

// ── Args ──────────────────────────────────────────────────────────────────────
const args = process.argv.slice(2);
function getArg(flag, defaultValue) {
  const idx = args.indexOf(flag);
  return idx !== -1 ? args[idx + 1] : defaultValue;
}

const DB_PATH   = resolve(getArg('--db',      'gsc_keywords.db'));
const WEBAPP    = resolve(getArg('--webapp',  '../../../stage-webapp'));
const LIMIT     = parseInt(getArg('--limit',  '10'), 10);
const DATE      = new Date().toISOString().split('T')[0];

// ── Load DB with sql.js ───────────────────────────────────────────────────────
async function loadDb(dbPath) {
  // Dynamic import — sql.js is a CJS module
  const { default: initSqlJs } = await import('sql.js');
  const SQL = await initSqlJs();
  if (!existsSync(dbPath)) {
    throw new Error(`DB not found: ${dbPath}`);
  }
  const buf = readFileSync(dbPath);
  return new SQL.Database(buf);
}

// ── Query top rising keywords per dialect ────────────────────────────────────
function queryDialectKeywords(db, dialect, limit) {
  const result = db.exec(`
    SELECT query, clicks_delta, clicks_delta_pct
    FROM   keyword_trends
    WHERE  dialect   = '${dialect}'
      AND  direction IN ('rising', 'new')
      AND  clicks_delta > 0
    ORDER  BY clicks_delta DESC
    LIMIT  ${limit}
  `);
  if (!result.length || !result[0].values.length) return [];
  return result[0].values.map(row => row[0]);
}

// ── Write src/config/seo-keywords.ts ─────────────────────────────────────────
function writeSeoKeywordsConfig(webapp, keywords) {
  const outPath = join(webapp, 'src/config/seo-keywords.ts');

  const lines = [
    `// Auto-generated by SEO weekly keyword analysis`,
    `// Last updated: ${DATE}`,
    `// Source: Google Search Console (14-day rolling window)`,
    `// DO NOT EDIT MANUALLY — regenerated weekly by GitHub Actions`,
    ``,
    `export const SEO_TRENDING_KEYWORDS: Record<string, readonly string[]> = {`,
  ];

  for (const [dialect, kws] of Object.entries(keywords)) {
    const quoted = kws.map(k => `    ${JSON.stringify(k)}`).join(',\n');
    lines.push(`  ${dialect}: [`);
    if (quoted) lines.push(quoted);
    lines.push(`  ],`);
  }

  lines.push(`} as const;`);
  lines.push(``);
  lines.push(`/**`);
  lines.push(` * Returns top trending GSC keywords for a given dialect.`);
  lines.push(` * Used in Next.js page metadata to improve keyword relevance.`);
  lines.push(` */`);
  lines.push(`export function getSeoKeywords(dialect: string): string[] {`);
  lines.push(`  const key = dialect.toLowerCase();`);
  lines.push(`  return [...(SEO_TRENDING_KEYWORDS[key] ?? [])];`);
  lines.push(`}`);
  lines.push(``);

  writeFileSync(outPath, lines.join('\n'), 'utf8');
  console.log(`✅ Written ${outPath}`);
  return outPath;
}

// ── Patch src/app/[lang]/[dialect]/page.tsx ──────────────────────────────────
function patchDialectPage(webapp) {
  const pagePath = join(webapp, 'src/app/[lang]/[dialect]/page.tsx');
  if (!existsSync(pagePath)) {
    console.warn(`⚠️  page.tsx not found at ${pagePath} — skipping patch`);
    return;
  }

  let src = readFileSync(pagePath, 'utf8');
  let changed = false;

  // 1. Add import (idempotent)
  if (!src.includes('@/config/seo-keywords')) {
    src = src.replace(
      'import { generateWebSiteSchema } from "@/utils/schema.utils";',
      'import { generateWebSiteSchema } from "@/utils/schema.utils";\nimport { getSeoKeywords } from "@/config/seo-keywords";'
    );
    changed = true;
  }

  // 2. Add keywords to generateMetadata return (idempotent)
  if (!src.includes('getSeoKeywords(')) {
    // Insert keywords field just before 'alternates:'
    src = src.replace(
      /(\s+)(alternates:\s*\{)/,
      '$1keywords: getSeoKeywords(dialectObj.enum),\n$1$2'
    );
    changed = true;
  }

  if (changed) {
    writeFileSync(pagePath, src, 'utf8');
    console.log(`✅ Patched ${pagePath}`);
  } else {
    console.log(`ℹ️  ${pagePath} already up-to-date`);
  }
}

// ── Main ─────────────────────────────────────────────────────────────────────
async function main() {
  console.log(`\n=== SEO Keyword Webapp Updater ===`);
  console.log(`DB:     ${DB_PATH}`);
  console.log(`Webapp: ${WEBAPP}`);
  console.log(`Date:   ${DATE}`);
  console.log(`Limit:  ${LIMIT} keywords/dialect\n`);

  const db = await loadDb(DB_PATH);

  const DIALECTS = ['haryanvi', 'rajasthani', 'bhojpuri', 'gujarati'];
  const keywords = {};

  for (const dialect of DIALECTS) {
    const kws = queryDialectKeywords(db, dialect, LIMIT);
    keywords[dialect] = kws;
    console.log(`  ${dialect}: ${kws.length} keywords`);
    if (kws.length) console.log(`    top: ${kws.slice(0, 3).join(', ')}`);
  }

  db.close();

  writeSeoKeywordsConfig(WEBAPP, keywords);
  patchDialectPage(WEBAPP);

  console.log('\n✅ Done — webapp SEO files updated');
}

main().catch(err => {
  console.error('❌ Error:', err.message);
  process.exit(1);
});
